def progressBar(seconds): 
  for i in range(100):
    iteration = i + 1
    output = "[" + "ඞ" * (100 * iteration // 100) + str("-" * (100 - (100 * iteration // 100))) + "] " + str(iteration)  + "%"
    print(output)
    time.sleep(seconds/100)
    print('\033[1A' + '\033[K', end='')
  os.system("clear")

